import React, { useEffect, useRef } from 'react'
import { Automat } from './Automat'
////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////
function Mont() {
    const canvasRef = useRef( null );
    useEffect(() => {
        const automat = new Automat({ 
            font: "29px monospace",
            ctx: canvasRef.current.getContext( "2d" ),
            offset: [ 40, 20 ],
            nfFrames: 5,
            delay: [ 100, 100 ], 
            fgr: "#bbf",
            bgr: "#494",
        });
    },[]);
    return (
        <canvas className="Mont"
                ref={ canvasRef }
                width="750"
                height="400">
        </canvas>
    );
}
////////////////////////////////////////////////////////////////
export { Mont }
